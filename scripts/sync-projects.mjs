#!/usr/bin/env node
import fs from "fs";
import path from "path";

const projectsDir = path.join("content", "projects");
const outFile = path.join("src", "data", "i18n", "projects.generated.ts");

if (!fs.existsSync(projectsDir)) {
  fs.mkdirSync(projectsDir, { recursive: true });
}

const files = fs.readdirSync(projectsDir).filter((f) => f.endsWith(".json")).sort();

const imports = files.map((file, i) => {
  const rel = `../../../content/projects/${file}`;
  return `import p${i} from "${rel}";`;
});

const entries = files.map((_, i) => `p${i}`);

const content = `// AUTO-GENERATED by scripts/sync-projects.mjs — do not edit manually
${imports.join("\n")}

import type { ProjectSource } from "./projects.types";

export const projectSources: ProjectSource[] = [${entries.join(", ")}];
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, content, "utf8");
console.log(`Synced ${files.length} project(s) → ${outFile}`);
